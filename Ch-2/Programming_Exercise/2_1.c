/* Write a program to display the equation of a line in the form 
 ax+by=c for a=5,b=8 and c =18 */

 #include<stdio.h>

 void main()
 {
    // variable initialization
    int a=5;
    int b=8;
    int c=18;

    // display the equation of a line
    printf("%dx + %dy = %d\n", a, b, c);

 }//end of function